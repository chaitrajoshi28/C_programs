//Program to print numbers from 1 to 10 using a for loop.

#include <stdio.h>

int main() {
    // Using a for loop to print numbers from 1 to 10
    for (int i = 1; i <= 10; i++) {
        printf("%d\n", i); // Print the current value of i
    }

    return 0; // Indicate that the program ended successfully
}

